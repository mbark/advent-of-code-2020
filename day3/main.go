func main() {
	trimmed := strings.Trim(input, "\n")
	rows := strings.Split(trimmed, "\n")
	fmt.Printf("first %d\n", first(rows))
	fmt.Printf("second %d\n", second(rows))
}

func treesHit(rows []string, colSlope, rowSlope int) int {
	treesHit := 0

	column := 0
	row := 0
	for {
		column += colSlope // right 3
		row += rowSlope    // down 1

		if row >= len(rows) {
			break
		}

		r := rows[row]

		if len(r) <= column {
			column = column % len(r)
		}

		pos := r[column]
		if pos == '#' {
			treesHit += 1
		}
	}

	return treesHit
}

func first(rows []string) int {
	return treesHit(rows, 3, 1)
}

func second(rows []string) int {
	return treesHit(rows, 1, 1) * treesHit(rows, 3, 1) * treesHit(rows, 5, 1) * treesHit(rows, 7, 1) * treesHit(rows, 1, 2)
}
